package com.camp.dto;

public class image {
    int reser_num;
    String image_name, image_userfile, image_save, image_type, image_size;

    public int getReser_num() {
        return reser_num;
    }

    public void setReser_num(int reser_num) {
        this.reser_num = reser_num;
    }

    public String getImage_name() {
        return image_name;
    }

    public void setImage_name(String image_name) {
        this.image_name = image_name;
    }

    public String getImage_userfile() {
        return image_userfile;
    }

    public void setImage_userfile(String image_userfile) {
        this.image_userfile = image_userfile;
    }

    public String getImage_save() {
        return image_save;
    }

    public void setImage_save(String image_save) {
        this.image_save = image_save;
    }

    public String getImage_type() {
        return image_type;
    }

    public void setImage_type(String image_type) {
        this.image_type = image_type;
    }

    public String getImage_size() {
        return image_size;
    }

    public void setImage_size(String image_size) {
        this.image_size = image_size;
    }

}
