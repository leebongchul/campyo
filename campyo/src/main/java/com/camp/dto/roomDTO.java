package com.camp.dto;

public class roomDTO {
    String room_name, room_camp, room_maxhead, room_image, room_intime, room_outtime;
    int room_price;

    public String getRoom_name() {
        return room_name;
    }

    public void setRoom_name(String room_name) {
        this.room_name = room_name;
    }

    public String getRoom_camp() {
        return room_camp;
    }

    public void setRoom_camp(String room_camp) {
        this.room_camp = room_camp;
    }

    public String getRoom_maxhead() {
        return room_maxhead;
    }

    public void setRoom_maxhead(String room_maxhead) {
        this.room_maxhead = room_maxhead;
    }

    public String getRoom_image() {
        return room_image;
    }

    public void setRoom_image(String room_image) {
        this.room_image = room_image;
    }

    public String getRoom_intime() {
        return room_intime;
    }

    public void setRoom_intime(String room_intime) {
        this.room_intime = room_intime;
    }

    public String getRoom_outtime() {
        return room_outtime;
    }

    public void setRoom_outtime(String room_outtime) {
        this.room_outtime = room_outtime;
    }

    public int getRoom_price() {
        return room_price;
    }

    public void setRoom_price(int room_price) {
        this.room_price = room_price;
    }

}
