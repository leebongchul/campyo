package com.camp.dto;

public class campDTO {
    String camp_name, camp_city, camp_kind, camp_image;

    public String getCamp_name() {
        return camp_name;
    }

    public void setCamp_name(String camp_name) {
        this.camp_name = camp_name;
    }

    public String getCamp_city() {
        return camp_city;
    }

    public void setCamp_city(String camp_city) {
        this.camp_city = camp_city;
    }

    public String getCamp_kind() {
        return camp_kind;
    }

    public void setCamp_kind(String camp_kind) {
        this.camp_kind = camp_kind;
    }

    public String getCamp_image() {
        return camp_image;
    }

    public void setCamp_image(String camp_image) {
        this.camp_image = camp_image;
    }

}
